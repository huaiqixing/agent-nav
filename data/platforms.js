// 从网站URL提取域名用于Logo
const getLogoUrl = (website) => {
  try {
    const url = new URL(website)
    const domain = url.hostname.replace('www.', '')
    return `https://logo.clearbit.com/${domain}`
  } catch {
    return null
  }
}

